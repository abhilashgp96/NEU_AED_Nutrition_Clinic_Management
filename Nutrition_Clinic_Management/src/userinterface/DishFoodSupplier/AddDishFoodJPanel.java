    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) dishFoodTble.getModel();

        model.setRowCount(0);

        for (Dish d : business.getDishLst().getDishLst()) {

            Object[] row = new Object[2];
            row[0] = d.getDishFoodId();
            row[1] = d;
            model.addRow(row);

        }

    }

