    private void addDishFoodBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addDishFoodBtnActionPerformed
        // TODO add your handling code here:

        String dishFoodName = drugtxtField.getText().trim();
        if(dishFoodName.isEmpty())
        {
            JOptionPane.showMessageDialog(null,"DishFood name cannot be empty");
            return;
        }
        ArrayList<String> dishFoodcheck= new ArrayList<>();
        for(Dish d:business.getDishLst().getDishLst())
        {
            dishFoodcheck.add(d.getDishFoodName().toLowerCase());
        }
        if(dishFoodcheck.contains(dishFoodName.toLowerCase()))
        {
            JOptionPane.showMessageDialog(null,"DishFood name already exists");
            return;
        }

        business.getDishLst().addDishLst().setDishFoodName(dishFoodName);
        
        populateTable();
        log.debug(userAccount+" "+"added new ishFood"+" "+dishFoodName);
        drugtxtField.setText("");

    }//GEN-LAST:event_addDishFoodBtnActionPerformed

    private void addIngredientButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addIngredientButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = dishFoodTble.getSelectedRow();
        if(selectedRow<0)
        {
            JOptionPane.showMessageDialog(null, "please select a row ");
            return;
        }
        Dish c = (Dish) dishFoodTble.getValueAt(selectedRow, 1);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        userProcessContainer.add("AddIngredientsJPanel", new AddIngredientsJPanel(userProcessContainer, business, userAccount, dishFoodOrganization, enterprise, network, c));
        log.debug(userAccount+" "+"entering add ingredients page");
        layout.next(userProcessContainer);
    }//GEN-LAST:event_addIngredientButtonActionPerformed

